#include <unistd.h>

void	putstr(char *str)
{
	int	j;

	j = 0;
	while (*(str + j) != '\0')
	{
		write(1, &str[j], 1);
		j++;
	}
}

int	main(int ac, char **av)
{
	int	i;

	i = ac - 1;
	while (i > 0)
	{
		putstr(av[i]);
		write(1, "\n", 1);
		i--;
	}
}
